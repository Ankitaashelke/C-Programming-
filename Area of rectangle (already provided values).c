///Area Of Rectangle///

#include <stdio.h>

int main()
{
    int length=8, breadth=4;

    int area = length*breadth;
    
    printf("length of rectangle is %d \n", length);
    printf("breadth of rectangle is %d \n", breadth);
    printf("area of rectangle is %d \n", area);
    
    return 0;
}   

